# Değer atamak için önce atanacak değeri yazıp sonra değerini eşitlememiz gerekir. Yani;

a = 5 # a'nın değeri 5 olarak atanır
b = 10 # b'nin değeri 10 olarak atanır
c = 15 # c'nin değeri 15 olarak atanır

d = a + b + c # d'nin değeri a, b ve c'nin toplamı olarak atanır

print(a)  # Çıktı: 5
print(b)  # Çıktı: 10  
print(c)  # Çıktı: 15


print(d)  # Çıktı: 30


print("-----------------------------------------------")


# Değerin yanına yazı da ekleyebilirsiniz. Yani;

print("a'nın değeri:", a)  # Çıktı: a'nın değeri: 5
print("b'nin değeri:", b)  # Çıktı: b'nin değeri: 10
print("c'nin değeri:", c)  # Çıktı: c'nin değeri: 15
print("a + b + c'nin toplamı:", d)  # Çıktı: a + b + c'nin toplamı: 30

